// While loop practice
using System;
					
public class Program
{
	public static void Main()
	{
		int i = 0;
		while (i<10)
		{
			Console.WriteLine(i);
			i++;
		}
	}
}

// Do while loop practice
using System;
					
public class Program
{
	public static void Main()
	{
		int i = 9;
		do
		{
			Console.WriteLine(i);
			i++;
		}
		while (i < 8);
	}
}
